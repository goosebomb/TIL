#include <stdio.h>
#include <stdbool.h>

void main9() {

	// bool
	// 논리형 자료형
	// true(참), false(거짓)을 저장할 수 있다.
	// #include <stdbool.h>를 적어야한다.
	// bool자료형의 변수이름 앞에는 is 혹은 b를 붙인다.
	bool isAttack = true;
	bool bRun = false;

	printf("isAttack : %d\n", isAttack);
	printf("bRun : %d\n", bRun);
	// c언어에서는 true는 1의 값, false는 0의 값을 가진다.	
}